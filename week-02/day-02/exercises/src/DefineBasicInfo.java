public class DefineBasicInfo {

  public static void main(String[] args) {
    // My name as a string
    String myName = "Marcell Mészáros";
    System.out.println("My name is: " + myName);
    // My age as an integer
    int myAge = 33;
    System.out.println("My age is: " + myAge + " years");
    // My height in meters as a double
    double myHeightInMeters = 1.91;
    System.out.println("My height is: " + myHeightInMeters + " meters");
    // Whether I am married or not as a boolean
    boolean amIMarried = false;
    System.out.println("Married: " + amIMarried);
  }
}
