public class TwoNumbers {
  public static void main(String[] args) {
    System.out.println(22 + 13); // 13 added to 22
    System.out.println(22 - 13); // 13 substracted from 22
    System.out.println(22 * 13); // 22 mutiplied by 13
    System.out.println(22 / 13.); // 22 divided by 13 (as decimal fraction)
    System.out.println(22 / 13); // integer part of 22 divided by 13
    System.out.println(22 % 13); // remainder of 22 divided by 13
  }
}
