        public static void FillMap(GameMap gameMap)
        {
            for (int row = 0; row < gameMap.CellMatrix.GetLength(0); row++)
            {
                for (int column = 0; column < gameMap.CellMatrix.GetLength(1); column++)
                {
                    gameMap.CellMatrix[row, column] =
                        (DefaultGameMap20[row, column] == 0) ? MapCell.Empty : MapCell.ShipHidden;
                }
            }
